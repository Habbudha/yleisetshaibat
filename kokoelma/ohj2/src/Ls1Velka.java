package ht2;

public class Ls1Velka {
	private int id;
	private String vid;
	private String pvm;
	private String mpvm;
	private int kuitattu;
	private int summa;
	
	public Ls1Velka(int id, String vid, String pvm, String mpvm, int kuitattu, int summa) {
		this.id = id;
		this.vid = vid;
		this.pvm = pvm;
		this.mpvm = mpvm;
		this.kuitattu = kuitattu;
		this.summa = summa;
	}
	public String toString() {
		return getId() + " " + getVid() + " " + getPvm() + " " + getMpvm() + " " + getKuitattu() + " " + getSumma();
	}
	public int getId() {
		return this.id;
	}
	public String getVid() {
		return this.vid;
	}
	public String getPvm() {
		return this.pvm;
	}
	public String getMpvm() {
		return this.mpvm;
	}
	public int getKuitattu() {
		return this.kuitattu;
	}
	public int getSumma() {
		return this.summa;
	}

}
